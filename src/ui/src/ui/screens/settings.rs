/// Spawn the platform file/URL opener for `p` on a detached thread so the UI
/// never blocks. Uses `explorer` on Windows, `open` on macOS, `xdg-open` elsewhere.
fn open_path(p: &str) {
    let p = p.to_string();
    std::thread::spawn(move || {
        let prog = if cfg!(target_os = "windows") {
            "explorer"
        } else if cfg!(target_os = "macos") {
            "open"
        } else {
            "xdg-open"
        };
        // `explorer` returns exit code 1 even on success, so we only log a hard
        // spawn failure and ignore the exit status itself.
        if let Err(e) = std::process::Command::new(prog).arg(&p).status() {
            log::warn!("{prog} {p} failed: {e}");
        }
    });
}

