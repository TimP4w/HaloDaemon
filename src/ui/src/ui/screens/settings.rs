// ── State ─────────────────────────────────────────────────────────────────────

#[derive(Default)]
pub struct SettingsUi {
    /// Which modal overlay is currently open.
    modal: Option<Modal>,
    /// Set once we've asked the daemon for `DebugInfo` to populate the
    /// dependency panel, so we don't re-request it every frame.
    deps_requested: bool,
    /// Cached OS "launch at login" state; seeded on first render so the
    /// toggle doesn't hit the filesystem/registry every frame. `None` until
    /// seeded.
    start_on_boot: Option<bool>,
}

enum Modal {
    Licenses,
    Credits,
    Diagnostics,
}

// ── Entry point ───────────────────────────────────────────────────────────────

pub fn show(
    ui: &mut egui::Ui,
    state: &AppState,
    cmd: &CommandTx,
    connected: bool,
    debug: Option<&DebugInfo>,
    st: &mut SettingsUi,
) {
    if !connected {
        st.deps_requested = false;
    }
    // Lazily pull a DebugInfo snapshot the first time Settings is shown while
    // connected; it feeds both the dependency panel and the diagnostics modal.
    if connected && !st.deps_requested {
        crate::domain::actions::system::get_debug_info(cmd);
        st.deps_requested = true;
    }

    egui::ScrollArea::vertical()
        .auto_shrink([false, false])
        .show(ui, |ui| {
            widgets::page_frame(ui, |ui| {
                page_body(ui, state, cmd, connected, debug, st);
            });
        });

    // ── Modals (drawn on top, not inside the scroll) ──────────────────────
    match st.modal {
        Some(Modal::Licenses) => {
            licenses_modal(ui.ctx(), st);
        }
        Some(Modal::Credits) => {
            credits_modal(ui.ctx(), st);
        }
        Some(Modal::Diagnostics) => {
            diagnostics_modal(ui.ctx(), debug, st);
        }
        None => {}
    }
}

