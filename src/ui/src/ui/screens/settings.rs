    /// Merge every `en` catalog file into the set of fully-qualified leaf keys
    /// it defines (mirrors how rust-i18n merges + flattens at load time).
    fn defined_en_keys() -> std::collections::BTreeSet<String> {
        fn flatten(
            prefix: &str,
            v: &serde_yaml::Value,
            out: &mut std::collections::BTreeSet<String>,
        ) {
            match v {
                serde_yaml::Value::Mapping(m) => {
                    for (k, val) in m {
                        let key = k.as_str().unwrap_or_default();
                        let path = if prefix.is_empty() {
                            key.to_string()
                        } else {
                            format!("{prefix}.{key}")
                        };
                        flatten(&path, val, out);
                    }
                }
                _ => {
                    out.insert(prefix.to_string());
                }
            }
        }
        let dir = format!("{}/locales", env!("CARGO_MANIFEST_DIR"));
        let mut out = std::collections::BTreeSet::new();
        for entry in std::fs::read_dir(&dir).unwrap() {
            let path = entry.unwrap().path();
            if path.extension().and_then(|e| e.to_str()) != Some("yaml") {
                continue;
            }
            let stem = path.file_stem().and_then(|s| s.to_str()).unwrap();
            if stem.split('.').next_back() != Some("en") {
                continue;
            }
            let v: serde_yaml::Value =
                serde_yaml::from_str(&std::fs::read_to_string(&path).unwrap()).unwrap();
            flatten("", &v, &mut out);
        }
        out
    }

    /// Extract every STATIC translation key (a string-literal first argument to
    /// the `t!` macro) used across `src/**/*.rs`. Keys containing `{` are skipped
    /// — those are interpolation-only forms, i.e. plain format strings.
    fn used_static_keys() -> std::collections::BTreeSet<(String, String)> {
        fn walk(dir: &std::path::Path, files: &mut Vec<std::path::PathBuf>) {
            for e in std::fs::read_dir(dir).unwrap() {
                let p = e.unwrap().path();
                if p.is_dir() {
                    walk(&p, files);
                } else if p.extension().and_then(|x| x.to_str()) == Some("rs") {
                    files.push(p);
                }
            }
        }
        let src = format!("{}/src", env!("CARGO_MANIFEST_DIR"));
        let mut files = Vec::new();
        walk(std::path::Path::new(&src), &mut files);

        let mut out = std::collections::BTreeSet::new();
        for f in files {
            let text = std::fs::read_to_string(&f).unwrap();
            let bytes = text.as_bytes();
            let mut i = 0;
            while let Some(rel) = text[i..].find("t!(") {
                let start = i + rel;
                i = start + 3;
                // Require a non-identifier char before `t!(` (else it's the tail
                // of `format!(`, `write!(`, etc.).
                if start > 0 {
                    let prev = bytes[start - 1];
                    if prev.is_ascii_alphanumeric() || prev == b'_' {
                        continue;
                    }
                }
                let rest = text[i..].trim_start();
                if let Some(after_quote) = rest.strip_prefix('"') {
                    if let Some(end) = after_quote.find('"') {
                        let key = &after_quote[..end];
                        // Static keys only; a `%{` placeholder means the value,
                        // not the key, interpolates — keys never contain `{`.
                        if !key.contains('{') {
                            out.insert((key.to_string(), f.display().to_string()));
                        }
                    }
                }
            }
        }
        out
    }

