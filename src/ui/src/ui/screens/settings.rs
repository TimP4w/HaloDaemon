/// Diagnostics modal — shows the full DebugInfo payload as formatted text.
fn diagnostics_modal(ctx: &egui::Context, debug: Option<&DebugInfo>, st: &mut SettingsUi) {
    modal_frame(
        ctx,
        &t!("settings.diagnostics_modal_title"),
        st,
        680.0,
        560.0,
        |ui| {
            match debug {
                None => {
                    ui.label(
                        egui::RichText::new(t!("settings.diagnostics_no_data"))
                            .font(theme::body(12.0))
                            .color(theme::TEXT_MUT),
                    );
                }
                Some(info) => {
                    // ── Copy / export ─────────────────────────────────────────
                    let copied_key = egui::Id::new("diag_copied_at");
                    ui.horizontal(|ui| {
                        if widgets::button(
                            ui,
                            &t!("settings.copy_json_button"),
                            widgets::ButtonKind::Ghost,
                            egui::vec2(110.0, 30.0),
                        )
                        .clicked()
                        {
                            ui.ctx().copy_text(diagnostics_json(info));
                            let now = ui.ctx().input(|i| i.time);
                            ui.ctx().data_mut(|d| d.insert_temp(copied_key, now));
                        }
                        ui.add_space(8.0);
                        if widgets::button(
                            ui,
                            &t!("settings.export_button"),
                            widgets::ButtonKind::Ghost,
                            egui::vec2(110.0, 30.0),
                        )
                        .clicked()
                        {
                            export_diagnostics(diagnostics_json(info));
                        }

                        let copied_at = ui.ctx().data(|d| d.get_temp::<f64>(copied_key));
                        let now = ui.ctx().input(|i| i.time);
                        if copied_feedback_visible(copied_at, now) {
                            ui.add_space(10.0);
                            ui.label(
                                egui::RichText::new(t!("settings.copied_feedback"))
                                    .font(theme::semibold(12.0))
                                    .color(theme::TRAFFIC_GREEN),
                            );
                            // Keep repainting so the confirmation clears without input.
                            ui.ctx().request_repaint();
                        }
                    });
                    ui.add_space(12.0);

                    // ── System ────────────────────────────────────────────────
                    section_label(ui, &t!("settings.diag_system"));
                    let sys = &info.system;
                    diag_card(ui, |ui| {
                        kv(ui, "OS", &format!("{:?} {}", sys.os, sys.os_version));
                        let elevated = if sys.running_elevated {
                            t!("settings.yes")
                        } else {
                            t!("settings.no")
                        };
                        kv(ui, &t!("settings.diag_elevated"), &elevated);
                        if let Some(p) = sys.pawnio_present {
                            let val = if p {
                                t!("settings.present")
                            } else {
                                t!("settings.absent")
                            };
                            kv(ui, "PawnIO", &val);
                        }
                        if let Some(u) = sys.udev_rules_present {
                            let val = if u {
                                t!("settings.present")
                            } else {
                                t!("settings.absent")
                            };
                            kv(ui, "udev rules", &val);
                        }
                        kv(ui, &t!("settings.diag_version"), &sys.daemon_version);
                        kv(ui, &t!("settings.diag_build"), &sys.daemon_build);
                    });

                    // ── Devices ───────────────────────────────────────────────
                    if !info.devices.is_empty() {
                        section_label(ui, &t!("settings.diag_devices", n = info.devices.len()));
                        diag_card(ui, |ui| {
                            for d in &info.devices {
                                let status = if d.connected {
                                    t!("settings.diag_connected")
                                } else {
                                    t!("settings.diag_offline")
                                };
                                mono_line(
                                    ui,
                                    &format!(
                                        "{} {} ({}) · {}",
                                        d.vendor, d.model, d.transport, status
                                    ),
                                    if d.connected {
                                        theme::TEXT
                                    } else {
                                        theme::OFFLINE_TEXT
                                    },
                                );
                                mono_line(ui, &format!("  {}", d.id), theme::TEXT_FAINT);
                                for (k, v) in &d.fields {
                                    mono_line(ui, &format!("    {k}: {v}"), theme::TEXT_DIM);
                                }
                            }
                        });
                    }

                    // ── HID entries ───────────────────────────────────────────
                    if !info.hid_entries.is_empty() {
                        section_label(ui, &t!("settings.diag_hid", n = info.hid_entries.len()));
                        diag_card(ui, |ui| {
                            for h in &info.hid_entries {
                                mono_line(
                                    ui,
                                    &format!(
                                        "{:04x}:{:04x}  page={:04x} usage={:04x}  {}",
                                        h.vid, h.pid, h.usage_page, h.usage, h.product
                                    ),
                                    theme::TEXT_DIM,
                                );
                            }
                        });
                    }

                    // ── SMBus buses ───────────────────────────────────────────
                    if !info.smbus_buses.is_empty() {
                        section_label(ui, &t!("settings.diag_smbus", n = info.smbus_buses.len()));
                        diag_card(ui, |ui| {
                            for b in &info.smbus_buses {
                                mono_line(
                                    ui,
                                    &format!(
                                        "Bus {} ({:?}): {}",
                                        b.bus_number, b.kind, b.adapter_name
                                    ),
                                    theme::TEXT_DIM,
                                );
                            }
                        });
                    }
                }
            }
        },
    );
}

/// A subtle inset panel wrapping one diagnostics section's rows.
fn diag_card<R>(ui: &mut egui::Ui, body: impl FnOnce(&mut egui::Ui) -> R) -> R {
    let r = egui::Frame::NONE
        .fill(theme::hex(0x080b10))
        .stroke(Stroke::new(1.0, theme::hex(0x1c2230)))
        .corner_radius(8.0)
        .inner_margin(egui::Margin::same(12))
        .show(ui, |ui| {
            ui.set_width(ui.available_width());
            body(ui)
        })
        .inner;
    ui.add_space(12.0);
    r
}

