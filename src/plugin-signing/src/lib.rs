pub const RELEASE_SCHEMA: u32 = 1;
/// Lua host/plugin ABI supported by HaloDaemon and its repository tooling.
/// Keep this value here so the daemon and standalone release tool cannot drift.
pub const PLUGIN_API: u32 = 2;
pub const SIGNATURE_ALGORITHM: &str = "ed25519";
pub const BUNDLE_METADATA_PATH: &str = ".halod-plugin-bundle.yaml";
const BUNDLE_MAGIC: &[u8] = b"HALODPLUG1\n";
pub const GENERATED_HEADER: &str = concat!(
    "# SPDX-License-",
    "Identifier: GPL-3.0-or-later\n",
    "# Generated by halod-plugin-signing. Do not edit.\n"
);

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct PackageHashMismatch {
    pub package: String,
    pub expected: String,
    pub actual: String,
}

impl std::fmt::Display for PackageHashMismatch {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "package '{}' hash mismatch: expected {}, got {}",
            self.package, self.expected, self.actual
        )
    }
}

impl std::error::Error for PackageHashMismatch {}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct PackageManifestMismatch {
    pub package: String,
    pub field: String,
    pub expected: String,
    pub actual: String,
}

impl std::fmt::Display for PackageManifestMismatch {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "repository package '{}' does not match its plugin.yaml {}: expected {}, got {}",
            self.package, self.field, self.expected, self.actual
        )
    }
}

impl std::error::Error for PackageManifestMismatch {}

/// Detached signature verification failed. Its stable type lets IPC clients
/// localize the user-facing error while logs retain the precise diagnostic.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct RepositorySignatureMismatch;

impl std::fmt::Display for RepositorySignatureMismatch {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str("release signature does not match release.yaml")
    }
}

impl std::error::Error for RepositorySignatureMismatch {}

